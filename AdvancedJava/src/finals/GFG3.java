package finals;

// Java program to demonstrate
// local final variable
// Main class
public class GFG3 {

    // Main driver method
    public static void main(String args[]) {
        // Declaring local final variable
        final int i;

        // Now initializing it with integer value
        i = 20;

        // Printing the value on console
        System.out.println(i);
    }
    //output: 20
}

